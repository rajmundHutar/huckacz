Změnili jsme prostory, zvolili jsme aulu katolického gymnázia, ale jinak jsme se poměrně drželi tradic. Stále někdo poskakoval po pódiu, tančilo se, objevili se recitátoři, komedianti i tragédi, někteří jedinci si s sebou brali saxofon, jiní maňásky a o přestávce herce vystřídal růžový panter.<br> 
Hráli jsme si na herce celé sobotní odpoledne, vzduch byl vydýchán, buchty i jiné laskominky od maminek všechny v našich útrobách, dort, jakožto ocenění nejlepšího hereckého výkonu, nemilosrdně rozkrájen, nejlepší divadlo vyhlášeno, takže Hučka 2003 našla svého majitele, otatní sošky a další ocenění se také rozutekly, brzy za nimi diváci, aktéři i veškeré další lidstvo celoodpolednímu klání přítomné. Vzpomínáme!<br>
