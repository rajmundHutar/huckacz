Šumící oceán, vlny se lámou o příď plujícího parníku, na přídi milenci, klavírní melodii doplňuje hlas zpívající cosi o tom, jak bez něj nemůže žít... V tom se Jack (čti džek) vyhoupl nad oponu, v náručí jeho milovaná Rose (čti rous). Chvíli se líbali, ale to stejně všichni znáte, ledovec, díra v lodi, jeden se utopí a střih!<br> 
